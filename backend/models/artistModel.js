import mongoose from "mongoose";

const artistSchema = mongoose.Schema({
    AID: {
        type: String,
        required: true,
        unique: true
    },
    name: {
        type: String,
        required: true
    },
    email: {
        type: String,
        required: true
    },
    phoneNumber: {
        type: Number,
        required: true
    },
    artistType: {
        type: String,
        required: true
    },
    username: {
        type: String,
        required: true,
        unique: true
    },
    password: {
        type: String,
        required: true
    },
},{ timestamps: true })

const Artist = mongoose.model('Artist', artistSchema)
export default Artist
    
